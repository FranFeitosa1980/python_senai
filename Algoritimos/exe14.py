# Desenvolva um algoritmo que peça ao usuário para digitar dois números e verifique se a soma deles é maior que 100.



numero_1 = int(input('Digite o primeiro numero: \n'))
numero_2 = int(input('Digite o segundo numero: \n'))


if numero_1 + numero_2 >= 100 : 
    print( 'A soma destes numeros é maior  que 100')

else:
    print('A soma destes numeros é menor  que 100')